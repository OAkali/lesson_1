public class Zoo {
    private String nameZoo;
    private  int countAnimals;

    public Zoo(String nameZoo, int countAnimals) {
        this.nameZoo = nameZoo;
        this.countAnimals = countAnimals;
    }

    public String getNameZoo() {
        return nameZoo;
    }

    public int getCountAnimals() {
        return countAnimals;
    }
}
